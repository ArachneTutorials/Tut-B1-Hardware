package lesson.datatypes;

@SuppressWarnings("unused")
public class NumericExamples {
    {
        int x = 15 % 4;
    }

    {
        int a = 46 / 13;
        int b = 17 + 72;
        int x = b % a;
    }

    {
        double a = 46 / 13;
        double b = 17 + 72;
        double x = b % a;
    }

    {
        double a = 46d / 13;
        double b = 17 + 72;
        double x = b % a;
    }
}