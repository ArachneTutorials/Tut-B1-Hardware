package lesson.functions.examples;

public class AssignmentOperators {
    public static void main(String[] args) {
        
        int x = 46;
        int y = 13;

        x++;
        x--;

        x += y;
        x -= y;
        x *= y;
        x /= y;
        x %= y;

        System.out.println(x);
    }
}